#!/usr/bin/env python3

'''
Type-annotated function concat that takes a string str1 and a string str2 as
arguments and returns their concatenation as a string.
'''


def concat(str1: str, str2: str) -> str:
    '''
    Returns the concatenation of two strings

    Parameters:
        str1 : str
        str2 : str

    Returns:
        str: concatenation of str1 and str2
    '''
    return str1 + str2
